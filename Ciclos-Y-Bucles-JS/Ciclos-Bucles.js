/*====================================================================================
WHILE
====================================================================================*/
function bucleWhile(num){
    let i=0;
    while(i<num){
        console.log(i);
        i++;
    }
}
bucleWhile(11);


/*====================================================================================
DO WHILE
====================================================================================*/
function doWhile(num){
    let i = 0;
    do{
        console.log(i);
        i++
    }while(i<num);
}
doWhile(11);


/*====================================================================================
FOR
====================================================================================*/
function bucleFor(num){
    for(let i=0; i<num; i++){
        console.log(i);
    }
}
bucleFor(11);