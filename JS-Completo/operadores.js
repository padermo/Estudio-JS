let a = 2;
let b = 4;

//Operadores Aritmeticos +, -, /, *, %
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);

//Operadores Relacionales >, <, <=, >=, ==, ===, !=, !==
console.log(a<b); //a es menor que b
console.log(a>b); //a es mayor que b
console.log(a<=b); //a es menor o igual a b
console.log(a>=b); //a es mayor o igual a b
console.log(a==b); //a es igual a b
console.log(a===b); //a es estrictamente igual a b
console.log(a!=b); //a es diferente de b
console.log(a!==b); //a es estrictamente diferente de b

//Operadores Logicos !-not, ||-or, &&-and
console.log(!true); //niega el valor de true e imprime false
console.log((2===2)||(2===2)); //cuando uno de los dos valores es verdadero, ejecuta
console.log((2===2)&&(2==="2")); //cuando los 2 valores son verdaderos, ejecuta